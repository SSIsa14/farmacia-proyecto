-- 2. Agregar columnas a USUARIO (solo si no existen)
DECLARE
   v_count NUMBER;
BEGIN
   -- PERFIL_COMPLETO
   SELECT COUNT(*) INTO v_count
   FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME = 'USUARIO'
     AND COLUMN_NAME = 'PERFIL_COMPLETO';

   IF v_count = 0 THEN
      EXECUTE IMMEDIATE 'ALTER TABLE USUARIO ADD PERFIL_COMPLETO CHAR(1) DEFAULT ''N''';
   END IF;
END;
/

DECLARE
   v_count NUMBER;
BEGIN
   -- PRIMER_LOGIN
   SELECT COUNT(*) INTO v_count
   FROM USER_TAB_COLUMNS
   WHERE TABLE_NAME = 'USUARIO'
     AND COLUMN_NAME = 'PRIMER_LOGIN';

   IF v_count = 0 THEN
      EXECUTE IMMEDIATE 'ALTER TABLE USUARIO ADD PRIMER_LOGIN CHAR(1) DEFAULT ''Y''';
   END IF;
END;
/
-- ===========================================

-- 3. Actualizar valores iniciales
BEGIN
   UPDATE USUARIO
      SET PERFIL_COMPLETO = 'Y',
          PRIMER_LOGIN = 'N'
    WHERE ACTIVO = 'Y';
   COMMIT;
END;
