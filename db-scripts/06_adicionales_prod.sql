-- 1. Tabla VERIFICATION_TOKEN (si no existe)
BEGIN
   EXECUTE IMMEDIATE '
      CREATE TABLE VERIFICATION_TOKEN (
          ID_TOKEN         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
          TOKEN            VARCHAR2(100) UNIQUE NOT NULL,
          ID_USUARIO       NUMBER NOT NULL,
          FECHA_CREACION   TIMESTAMP DEFAULT SYSTIMESTAMP,
          FECHA_EXPIRACION TIMESTAMP NOT NULL,
          FECHA_VERIFICACION TIMESTAMP,
          CONSTRAINT fk_token_usuario FOREIGN KEY (ID_USUARIO) REFERENCES USUARIO(ID_USUARIO)
      )
   ';
EXCEPTION
   WHEN OTHERS THEN
      IF SQLCODE = -955 THEN NULL; -- Table already exists
      ELSE RAISE;
   END IF;
END;
/

-- Índices (si no existen)
DECLARE
   v_count NUMBER;
BEGIN
   -- No crear índice sobre TOKEN porque ya existe por UNIQUE
   SELECT COUNT(*) INTO v_count
   FROM USER_INDEXES
   WHERE INDEX_NAME = 'IDX_VERIFICATION_USUARIO';

   IF v_count = 0 THEN
      EXECUTE IMMEDIATE 'CREATE INDEX IDX_VERIFICATION_USUARIO ON VERIFICATION_TOKEN(ID_USUARIO)';
   END IF;
END;
/

